    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        String precio4 = jTextField4.getText();
        String precio5 = jTextField5.getText();
        String precio6 = jTextField6.getText();
        String sql = "UPDATE configuracion SET mani='" + precio4 + "' , pedi='" + precio5 + "' , figu='" + precio6 + "'  ";
        int punto = 0;

        for (int o = 1; o <= precio4.length(); o++) {
            
            if (precio4.substring((o - 1), o).equals(".") || precio4.substring((o - 1), o).equals(",")) {
               
                punto = 1;
            }
        }
        for (int o = 1; o <= precio5.length(); o++) {
          
            if (precio5.substring((o - 1), o).equals(".") || precio5.substring((o - 1), o).equals(",")) {
                
                punto = 2;
            }
        }
        for (int o = 1; o <= precio6.length(); o++) {
           
            if (precio6.substring((o - 1), o).equals(".") || precio6.substring((o - 1), o).equals(",")) {
                
                punto = 3;
            }
        }
        switch (punto) {
            case 1:
                JOptionPane.showMessageDialog(jPanel7, "Valor no valido en el campo 1 quitar puntos", "Alerta", JOptionPane.ERROR_MESSAGE);
                break;
            case 2:
                JOptionPane.showMessageDialog(jPanel7, "Valor no valido en el campo 2 quitar puntos", "Alerta", JOptionPane.ERROR_MESSAGE);
                break;
            case 3:
                JOptionPane.showMessageDialog(jPanel7, "Valor no valido en el campo 3 quitar puntos", "Alerta", JOptionPane.ERROR_MESSAGE);
                break;
            default:
                try {
                    conexion.getSentencia().executeUpdate(sql);
                    JOptionPane.showMessageDialog(jPanel7, "Cambios Exitosos", "Cambios", JOptionPane.PLAIN_MESSAGE);

                } catch (SQLException j) {
                    JOptionPane.showMessageDialog(jPanel7, "Valor invalido", "Alerta", JOptionPane.ERROR_MESSAGE);
                }
                break;
        }

    }//GEN-LAST:event_jButton5ActionPerformed

